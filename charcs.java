package character;

public class charcs {
    public static void main(String[] args) {
        // Character cg= new Character('a');

        //isLetter function
        System.out.println(Character.isLetter('q'));
        System.out.println(Character.isLetter('2'));

        // isdigit

        System.out.println(Character.isDigit('w'));
        System.out.println(Character.isDigit('0'));

        // isuppercase
        System.out.println(Character.isUpperCase('w'));

        //islowercase
        System.out.println(Character.isLowerCase('w'));

        // touppercase
        System.out.println(Character.toUpperCase('w'));



    }
}
