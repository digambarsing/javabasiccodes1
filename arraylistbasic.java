package arraylist;

public class arraylistbasic {
    public static void main(String[] args) {
        // wrapper class
        // it is used to create a variable with the help of class as a object by wrapping var into class

        Integer n=Integer.valueOf(3);

        System.out.println(n);

        Float p=Float.valueOf(2.3f);

        System.out.println(p);
    }
}
